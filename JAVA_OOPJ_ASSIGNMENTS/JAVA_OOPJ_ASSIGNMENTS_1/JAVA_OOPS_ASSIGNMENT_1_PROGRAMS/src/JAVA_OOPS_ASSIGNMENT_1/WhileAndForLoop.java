package JAVA_OOPS_ASSIGNMENT_1;

//11. Print numbers from 1 to 50 using: 
//● for loop 
//● while loop 

public class WhileAndForLoop {

	public static void main(String[] args) {
		
		int i;
		
		for(i=1;i<=50;i++)
		{
			System.out.println(i + " ");
		}
   int j=1;
		
		while(j<=50)
		{
			System.out.println(j + " ");
			j++;
		}
	}

}
