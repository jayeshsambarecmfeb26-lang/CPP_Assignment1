package JAVA_OOPS_ASSIGNMENT_1;

//12. Write a program to print sum of first N natural numbers. 

public class NaturalNumbarSum {

	public static void main(String[] args) 
	{
		  int n=7,sum=0;
		  
	        for(int i=1;i<=n;i++) 
	        	
	        	sum+=i;
	        
	        System.out.println("Natural Number Sum="+sum);

	}

}
