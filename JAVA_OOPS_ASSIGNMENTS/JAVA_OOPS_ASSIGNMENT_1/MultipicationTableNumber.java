package JAVA_OOPS_ASSIGNMENT_1;

// Write a program to print multiplication table of a number. 

public class MultipicationTableNumber {

	public static void main(String[] args)
	{
        int num=7;
        for(int i=1;i<=10;i++)
        {
            System.out.println(num+" x "+i+" = "+(num*i));
    }
	
	}

}
