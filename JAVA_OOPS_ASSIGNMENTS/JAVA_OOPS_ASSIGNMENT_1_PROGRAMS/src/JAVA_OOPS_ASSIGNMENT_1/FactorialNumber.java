package JAVA_OOPS_ASSIGNMENT_1;
 
//17. Write a program to find factorial of a number. 

public class FactorialNumber {

	public static void main(String[] args)
	{
		
        int fact=1;
        
        for(int i=1;i<=7;i++) 
        	
        	fact*=i;
        
        System.out.println("Factorial="+fact);//Factorial=5040
		

	}

}
